function toISO(year: number, ddmm: string): { iso: string; month: number } {
  const [d, m] = ddmm.split(".").map(Number);
  return { iso: `${year}-${String(m).padStart(2, "0")}-${String(d).padStart(2, "0")}`, month: m };
}

/** Expand the compact rows for one (year, state) into full FerienPeriods. */
export function expandPeriods(year: number, rows: Row[]): FerienPeriod[] {
  return rows.map(([t, s, e]) => {
    const start = toISO(year, s);
    if (!e) {
      return { typ: TYP[t], start: start.iso, end: start.iso, note: "beweglicher Ferientag" };
    }
    // Weihnachtsferien and any Dec→Jan span roll over into the next year.
    const endYear = toISO(year, e).month < start.month ? year + 1 : year;
    const end = toISO(endYear, e);
    return { typ: TYP[t], start: start.iso, end: end.iso };
  });
}
